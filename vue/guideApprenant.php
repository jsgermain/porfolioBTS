

<h3 class="centrer"> Guide d’utilisation de Chamilo pour les apprenants </h3>

<h4> Visualiser un cours, doc et vidéo / ouvrir dans un nouvel onglet </h4>
Aller dans l’onglet « Mes cours » <br/>
Sélectionner le cours (exemple : Développement). 

<br/><br/>


<h4>Fichier PDF </h4>
L’apprenant peut le télécharger, l’ouvrir dans une nouvelle fenêtre, le copier dans son nouveau dossier de fichiers et le prévisualiser.
    <ul>

        <li>  Ouvrir  dans une nouvelle fenêtre </li>
        <li>  Copier dans mon dossier personnel de fichiers </li>
        <li>  Télécharger </li>
        <li>  Prévisualiser </li>
        <li>  Télécharger (télécharger en un clic tous les fichiers PDF dans un format zip) </li>
    </ul>


<h4>Trier ses cours</h4>
    <ul>
        <li> Via la « Page d’accueil » ou l’onglet « Mes cours »
        <li> Aller dans « Cours » dans la barre latérale
        <li> Cliquer sur « Trier mes cours »
        <li>Hiérarchiser les cours, à l’aide des flèches, monter et/ou descendre un ou plusieurs cours.
    </ul>
<br/><br/>
 

<h4>Créer une catégorie personnelle de cours </h4>
    <ul> 
        <li> Via la « Page d’accueil » ou l’onglet « Mes cours »
        <li> Aller dans « Cours » dans la barre latérale
        <li> Cliquer sur « Trier mes cours »
        <li> Cliquer sur le témoin « Créer une catégorie personnelle de cours ».
    </ul>

<br/> <br/> 

<h4>Inscription au cours </h4>
    <ul>
        <li> Aller sur la page d’accueil
        <li> Sélectionner le ou les cours comportant la case « s’inscrire ».
    </ul>

 <br/><br/>

 
<h4>Accéder à sa boite de réception </h4> 
    <ul>
        <li> Aller sur l’onglet « Page d’accueil » ou « Mes cours » 
        <li> Aller dans « Profil » dans la barre latérale
        <li> Cliquer sur « Boite de réception » 
<h4> Ou </h4>
        <li>Aller sur l’onglet « Réseau social » 
        <li> Aller dans « Réseau social » dans la barre latérale
        <li> Cliquer sur « Messages »
    </ul>
 
<br/><br/>

<h4>Envoyer un message </h4>
    <ul> 
        <li>Un message ne peut envoyer qu’aux utilisateurs du site (prof et apprenant)
        <li> Cliquer sur « composer » dans la barre latérale « profil » et cette page s’ouvre
    </ul>

<br/> <br/>

Cette page précédente est aussi accessible en : <br/>
Cliquant sur« Réseau social »,  <br/>
Sélectionnant « Messages » dans la barre latérale, <br/>
Cliquant sur le témoin « composer message ».  <br/>
 
