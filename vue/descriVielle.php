
<h1> Qu’est-ce-que la veille technologique </h1>


<img src="images/veille-technologique-660x330.jpg" alt="veille-technologique-660x330.jpg" class=""/>

<p class="justifier"> 
La veille technologique consiste à s’informer de façon systématique sur les nouvelles techniques dans le but d’être toujours 
au courant des dernières avancées technologiques ainsi qu’être le meilleur dans son domaine professionnel. Aujourd’hui, la veille 
technologique possède une place majeure dans notre domaine que ce soit pour les SLAM ou pour les SISR. <br>

Une veille peut-être active ou passive

<li> Elle est passive dans la mesure où on s'en sert pour se maintenir au courant d'un thème sans but spécial.

<li> Elle est active dans le cas où elle se fait dans un but précis pour construire quelque chose sur un thème quelconque. </li>

La veille technologique sert à : <br>

 <li> Se tenir au courant des dernières mises à jour de produits

 <li> Découvrir des solutions plus avantageuses

 <li> Anticiper sur la concurrence : on peut ainsi rester à la pointe du marché en acquérant toutes les nouvelles technologies qui permettent 
     de disposer d’un avantage concurrentiel.

 <li> Se former sur un thème

