Gestion libre de parc informatique

<p class=justifier>
GLPI est une application web qui aide les entreprises à gérer leur système d’information. 
Parmi ses caractéristiques, cette solution est capable de construire un inventaire de toutes les ressources de la société et de réaliser 
la gestion des tâches administratives et financières. Les fonctionnalités de cette solution aident les 
Administrateurs IT à créer une base de données regroupant des ressources techniques et de gestion, ainsi qu’un historique des 
actions de maintenance. La fonctionnalité de gestion d'assistance ou helpdesk fournit aux utilisateurs un service leur permettant 
de signaler des incidents ou de créer des demandes basées sur un actif ou non, ceci par la création d'un ticket d’assistance
</p>