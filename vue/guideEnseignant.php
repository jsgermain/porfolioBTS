

<h3 class="centrer"> Guide d’utilisation de Chamilo pour les enseignants </h3>

<h4>Créer un cours</h4>

Après connexion, cliquer sur le lien « Créer un cours ». <br/>
Donner le nom du cours, puis cliquer sur « Créer un cours ». <br/>
Cliquer sur « Description de cours ».<br/>
Cliquer sur l’onglet « Description du cours » (le 1er carré à gauche)<br/>
Création de cours – insertion de vidéos).<br/>
Vidéos Youtube).<br/>
Cliquer sur le symbole Youtube.<br/>
Coller le lien de la vidéo Youtube sur un des deux emplacements (pour plus de facilité, choisir le deuxième : « Coller 
l’url de la vidéo ici »). Puis cliquer sur OK.<br/>
La miniature de la vidéo est collée dans le contenu. Cliquer sur Enregistrer (ne pas oublier de mettre le titre).<br/>
Retourner au début (cliquer sur le cours que vous venez de mettre à jour, puis sur « Description du cours »).<br/>
La vidéo Youtube est à présent affichée et disponible pour visionnage.<br/>


<h4>Création de cours – insertion de liens HTML</h4>
Donner le nom du cours, puis cliquer sur « Créer un cours ». Si le cours est déjà créée, cliquer sur le cours concerné.<br/>
Cliquer sur « Sommaire du cours ».<br/>
Cliquer sur « Description de cours ».<br/>
Cliquer sur l’onglet « Description du cours » (le 1er carré à gauche).<br/>
Cliquer sur le symbole « Lien » (ne pas oublier le titre).<br/>
Coller le lien HTML dans l’emplacement URL, puis cliquer sur OK.<br/>
Cliquer sur « Enregistrer ».<br/>
La description du cours a été mise à jour.<br/>
Le lien HTML souhaité est disponible dans le cours.<br/>


<h4>Alternative – Insertion de liens HTML).</h4>
Cliquer sur « Documents ».<br/>
Cliquer sur la rollbar « Répertoire courant » et sur votre nom d’enseignant dans « Répertoire des utilisateurs ».<br/>
Cliquez sur « Créer un document ».<br/>
Après avoir indiqué le titre et cliqué sur le symbole Lien, coller le lien HTML dans l’emplacement URL, puis cliquer sur OK.<br/>
Le fichier est disponible dans le répertoire de l’enseignant.<br/>
 

<h4>Profil Enseignant</h4>
Création de répertoires)<br/>
Pour organiser vos cours et exercices, il vous faut créer des répertoires.<br/>
Pour créer un répertoire, il faut commencer par créer un cours. Après la création, cliquer sur l’onglet « Documents ».<br/>
Sur l’onglet à côté de « Répertoire courant », cliquer sur votre nom de formateur.<br/>
Cliquer sur l’icône du bouton correspondant à « Créer un répertoire ».<br/>
Donnez un titre à votre répertoire, puis cliquer sur « Créer le dossier ».<br/>
Vous pouvez créer plusieurs sous-répertoires dans vos répertoires.<br/>
 

<h4>Dépôt de fichiers PDF</h4>
Dans vos répertoires, vous pouvez déposer des fichiers PDF (pour vos cours par exemple).<br/>
Cliquez sur « Cliquez-déplacez des fichiers ici pour les envoyer au serveur ».<br/>
Vous verrez affiché « Envoi du fichier réussi ! ».<br/>
Le fichier PDF est accessible dans le dossier (par exemple : « Cours »).<br/>
 

<h4> Trier des cours (importation dans des catégories de cours)</h4>
Cliquer sur « Trier mes cours ».<br/>
A côté de l’intitulé du cours récemment créée, cliquer sur « Editer » (symbole du crayon ».<br/>
Cliquer sur la rollbar (où il y a indiqué « pas de catégorie en cours ») et choisir la catégorie désirée. 
Puis cliquer sur « Valider ».<br/>
Le cours est maintenant associé à la catégorie.<br/>


<h4> Inscription d’élèves à un cours</h4>
Cliquez sur votre cours.<br/>
Cliquez sur l’onglet « Utilisateurs ».<br/>
Cliquez sur « Ajouter ».<br/>
Dans la liste des apprenants, cliquez sur « Inscrire » à côté de l’élève que vous souhaitez inscrire à votre cours.<br/>
L’élève sélectionné a été inscrit au cours.<br/>


<h4>Suppression de cours</h4>
Cliquer sur « Maintenance »<br/>
Pour supprimer un cours, cliquer sur « Détruire un cours de manière irréversible ». Il sera supprimé du site.
