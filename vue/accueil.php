
	<h1 class="centrer">BIENVENUE SUR MON PORTFOLIO<h1>

	<h4 class="centrer">DEVELOPPEUR SOLUTIONS LOGICIELLES ET APPLICATIONS</h4>

	
	<p class="justifier">Je suis actuellement en première année de BTS SIO (Services Informatiques aux Organisations) spécialité SLAM (solutions logicielles et applications métiers). 
	Une formation me permet d'acquérir des compétences dans la programmation, la sécurité des réseaux, les bases de données et surtout la maintenance d’un système 
	d’information dans une entreprise. <br>
	<br><br>


	Le BTS SIO ( Services Informatiques aux Organisations à été créé en 2011, anciennement connu sous le nom de BTS IG ( Informatique de Gestion ). Il propose deux options: <br>


<ul class="justifier">
<li>SISR « solutions d’infrastructure, systèmes et réseaux »</li>
<li>SLAM « solutions logicielles et applications métiers »</li>
</ul>
	<p class="justifier">
		Cette fillière consiste à acquérir les compétences professionnelles permettant de préparer à l'installation, l'intégration, l'administration, la sécurisation 
	et la mesure des performances des équipements. <br><br>

	<p class="justifier">Le but est être capable de sélectionner et d'installer un serveur et d'administrer le réseau (configuration matérielle et logicielle, administration, sécurité). 
	C'est un spécialiste de l'exploitation et de la surveillance des équipements connectés, ainsi que de l'assistance aux utilisateurs. Ses interventions sont fondées 
	sur des critères de sécurité, de maintenabilité, d'optimisation des performances et d'adaptabilité. <br>
	<br><br>

	<p class="justifier">Acquérir les compétences professionnelles permettant de préparer à la fourniture de services couvrant principalement la conception et le développement de logiciels 
	dans le domaine de l'informatique de gestion.
	<br><br>


Le titulaire du BTS SIO option SLAM doit être capable de suivre l'ensemble du cycle de vie des applications : 

<ul class="justifier">
<li>Analyser les besoins des utilisateurs finaux;</li>
<li>Rechercher et choisir une solution adaptée;</li>
<li>Concevoir et développer des applications (génie logiciel)</li>
<li>Assurer la maintenance des programmes qui garantissent le bon fonctionnement de ces applications. </li>
</ul>
</p>
