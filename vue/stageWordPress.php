La première partie de stage consistait à faire le site de la formation du BTS SIO de l'École Nationale du Commerce (ENC). Mon binôme et moi avons 
utilisé un système de gestion de contenu (SGC) ou Content Management System (CMS) en anglais regroupe une catégorie de logiciels qui permettent 
de concevoir, gérer et mettre à jour des sites Web ou des application mobile de manière dynamique. Nous avons utilisé le CMS WordPress.


<h4> C’est quoi WordPress ? </h4>
<p class=justifier>


WordPress est un système de gestion de contenus permettant de créer son propre site assez facilement. Il est facile à utiliser, 
Libre, gratuit et personnalisable.
</p>

<h4> Utiliser WordPress pour faire quoi ? </h4> 
<p class=justifier> WordPress donne la possibilité à ses utilisateurs de faire ses prpopres mises à 
jour ou modifications, de publier ses News, de gérer ses Newsletters en tout simplicité.<br> 
Voici une petite liste de ce que nous pouvons faire WordPress

    <ul>
        <li> un Blog, un Site vitrine, un e-Commerce
        <li> un Forum, un site de Petites Annonces
        <li> un Magazine en ligne
        <li> une Plate-forme d’échange
        <li> un Annuaire, un WiKi
        <li> un Réseau social, un Intranet
        <!--<li> voir des exemples de sites WordPress. -->
    </ul>
</p>

<h4> Pourquoi WordPress est-il si prisé ? </h4>

<p class=justifier>
Le succès que connait WordPress est dû à plusieurs de ses avantages qui facilitent largement le travail de ses utilisateurs à plusieurs niveaux :
    <ul>
        <li> Côté blogueur : une personne qui se sent l’âme d’un blogueur pourrait facilement, et sans grand budget, créer et maintenir 
            un blog de qualité avec WordPress.
        <li> Côté entreprise : une entreprise qui ferait appel à une agence, pour créer son site, pourrait facilement modifier son contenu 
            sans constamment faire appel au créateur : une liberté très appréciée, plébiscitée même !
        <li> Côté créateur de site : les freelances ou agences web peuvent facilement créer des sites pour leurs clients sans devoir coder l
            e cœur du framework ainsi que le back-office. C’est ce que l’on appelle : “arrêter de réinventer la roue”.
        <li> Côté développeur : WordPress est le terrain de jeu idéal pour les développeurs créatifs. Ce CMS permet de faire du développement 
            sur mesure pour obtenir des sites ultra puissants et fonctionnels.
        <li> Côté e-commerce : bien que cette partie a pu faire débat, autrefois, avec quelques comparaisons avec Prestashop ou Magento, que 
            vous le vouliez ou non, l’extension WooCommerce vous permettra de faire de vrais sites marchands, il n’y a pas de doutes !
    </ul>
</p>

<h4> Les principaux défauts de WordPress sont :</h4>
<p>
    <ul> 
        <li> les possibles problèmes de lourdeur liés à l’abus de plugins. 
        <li> La recherche au sein du site n’est pas vraiment optimisée. L’ajout d'une extension de recherche permet de pallier à ce manque, si besoin. 
        <li> Le formulaire de contact est quant à lui inexistant dans la version de base. 
        <li>La gestion des utilisateurs et des médias pourrait/devrait être améliorée.
    </ul>
</p>