
Pour mon deuxième stage dont le but était de poursuivre le projet de mon premier stage étant de faire le site de la formation du BTS SIO de l'École 
Nationale du Commerce (ENC). Mon binôme et moi avons utilisé un système de gestion de contenu (SGC) ou Content Management System (CMS) 
en anglais regroupe une catégorie de logiciels qui permettent de concevoir, gérer et mettre à jour des sites Web ou des application mobile 
de manière dynamique. Nous avons utilisé un CMS spécialisé dans l'aprrentissage Chamilo.


<h4> C’est quoi Chamilo ? </h4>

<p class=justifier>

Chamilo est un logiciel open source de gestion de l'apprentissage et du contenu d'apprentissage, 
dont l'objectif est d'améliorer l'accès global à l'éducation et au savoir. Il est soutenu par l'Association Chamilo, 
dont les objectifs incluent la promotion du logiciel, le maintien d'un canal de communication clair et la construction d'un réseau de 
fournisseurs de services et de contributeurs au logiciel.

<h4> Particularités </h4>
<p class=justifier>

Le projet Chamilo LMS est une suite éducative simple d'utilisation, pour tous les publics (autant publics que privés, 
autant personnels que méga-corporate), ce qui se traduit par une interface conviviale (simple et colorée) qui peut être 
redéfinie par ses utilisateurs selon leurs besoins. <br>

L'Association Chamilo3 et ses membres poursuivent l'objectif de développer des outils permettant de pallier les défauts logistiques 
actuels de l'éducation dans le monde, et les statuts de l'association soulignent cette particularité, qui rend le projet Chamilo 
particulièrement orienté vers la libre éducation et l'accès à l'éducation pour tous.
</p>


