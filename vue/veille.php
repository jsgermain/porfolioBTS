<h4> VEILLE TECHNOLOGIQUE – LA RÉALITÉ VIRTUELLE </h4>

<p class="justifier">Dans le cadre de la prépration de mon BTS Services Informatiques aux Organisations, je dois effectuer une veille 
    technologique informatique. Le thème choisi pour ma veille technologique est portée sur le sujet de la réalité virtuelle. La réalité 
    virtuelle est une technologie pour laquelle vous devez mettre un casque sur votre tête afin de vous plonger dans un autre univers. 
    Le casque enregistre chaque mouvement de votre tête, ce qui donne l'impression que vous êtes vraiment dans un autre monde. Vous mettez
     un casque et regardez autour de vous à 360 degrés et expérimentez tout en 3D. Il existe de nombreuses possibilités de jeu, d'expérience 
     et de conception. 
     
     <br><br>

Afin d’organiser ma veille technologique, j’utilise différents outils de veille :

<li>
    Google Alerts : Outil de veille plutôt basique. J’utilise les mots clés « réalité virtuelle ». Cependant il n’est 
pas très pertinent et est irrégulier dans les informations. Un jour on peut avoir aucune notification alors que le lendemain on peut 
en avoir 15 en même temps. 

<li> Netvibes : L’outil de veille que j’utilise principalement. Je me sers des mêmes mots clés que Google Alterts et les informations sont plus régulières et pertinentes.

<li> Twitter : J’utilise Twitter pour follow les comptes qui parlent de sécurité informatique  et avoir des informations grâce aux  » # « .
    
<li class="justifier"> Pocket : Pocket est une application qui permet de sauvegarder du contenu Web intéressant (des articles, des vidéos) pour les consulter 
    ultérieurement. Une fois les éléments sauvegardés dans Pocket, la liste de contenu est accessible sur n’importe quel appareil : 
    téléphone, tablette ou ordinateur. Les éléments peuvent être consulté de n’importe où (dans les transports en commun, en voyage, 
    sur le canapé).

<li> AppyGeek : Application mobile (Android et iOS) regroupant toutes les actualités informatiques. On peut personnaliser notre environnement 
    de news, de plus l’application est très belle graphiquement et très ergonomique. 

</p>





